// Declaring Variables in JavaScript

/**
 * 1. Using 'var' keyword
 * 2. Using 'let' keyword
 * 3. Using 'const' keyword
 */


// var is mutable variable
// mutable bhaneko define garisakepachi  value change garna milne
var name = "Sarita Sharma";
// ...


name = 1;

// let is also mutable variable

let lastName = "Sarita Sharma";

lastName = 1;

// const is immutable variable
// cannot change variable value on runtime


const a = "123";

a = 123;